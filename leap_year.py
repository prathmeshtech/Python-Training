# Mod of 4 and not divisble by 100
# Mod of 400 should be zero

def identify_leap_year(year):
    if year % 4 == 0:
        if year % 100 == 0:
            if year % 400 == 0:
                return True
            else:
                return False
        else:
            return True
    else:
        return False
